import json

import plotly.express as px
# -



# +


# Path to the JSON file generated by Scalene
json_file_path = "path/to/profile.json"

# Read and parse the JSON file
with open(json_file_path, "r") as json_file:
    profile_data = json.load(json_file)

# Extract memory usage data from the JSON
memory_usage_data = [entry["memory"]["rss"] for entry in profile_data]

# Create a list of time intervals (assuming data points are collected at regular intervals)
time_intervals = list(range(len(memory_usage_data)))

# Create a Plotly line chart
fig = px.line(
    x=time_intervals,
    y=memory_usage_data,
    title="Memory Usage Over Time",
    labels={"x": "Time Interval", "y": "Memory Usage (bytes)"},
)

# Show the chart
fig.show()
